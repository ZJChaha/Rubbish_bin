void can_filter_init(void)
{

    CAN_FilterTypeDef can_filter_st;
    can_filter_st.FilterActivation = ENABLE;
    can_filter_st.FilterMode = CAN_FILTERMODE_IDMASK;
    can_filter_st.FilterScale = CAN_FILTERSCALE_32BIT;
    can_filter_st.FilterIdHigh = 0x0000;
    can_filter_st.FilterIdLow = 0x0000;
    can_filter_st.FilterMaskIdHigh = 0x0000;
    can_filter_st.FilterMaskIdLow = 0x0000;
    can_filter_st.FilterBank = 0;
    can_filter_st.FilterFIFOAssignment = CAN_RX_FIFO0;
    HAL_CAN_ConfigFilter(&hcan1, &can_filter_st);
    HAL_CAN_Start(&hcan1);
    HAL_CAN_ActivateNotification(&hcan1, CAN_IT_RX_FIFO0_MSG_PENDING);

    can_filter_st.SlaveStartFilterBank = 14;
    can_filter_st.FilterBank = 14;
    HAL_CAN_ConfigFilter(&hcan2, &can_filter_st);
    HAL_CAN_Start(&hcan2);
    HAL_CAN_ActivateNotification(&hcan2, CAN_IT_RX_FIFO0_MSG_PENDING);
}




void Can_send_data(int16_t motor1, int16_t motor2, int16_t motor3, int16_t motor4)
{

    uint32_t mail_box;
	tx_message.StdId=0x200;
	tx_message.IDE=CAN_ID_STD;
	tx_message.DLC=0x08;
	tx_message.RTR=CAN_RTR_DATA;
	M3508_DATA[0]=motor1>>8;
	M3508_DATA[1]=motor1;
	M3508_DATA[2]=motor2>>8;
	M3508_DATA[3]=motor2;
	M3508_DATA[4]=motor3>>8;
	M3508_DATA[5]=motor3;
	M3508_DATA[6]=motor4>>8;
	M3508_DATA[7]=motor4;
    HAL_CAN_AddTxMessage(&hcan1, &tx_message, M3508_DATA, &mail_box);

}

