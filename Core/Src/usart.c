void usart_printf(const char *fmt,...)
{
    static uint8_t tx_buf[256] = {0};
    static va_list ap;
    static uint16_t len;
    va_start(ap, fmt);


    len = vsprintf((char *)tx_buf, fmt, ap);

    va_end(ap);

HAL_UART_Transmit_DMA(&huart1,tx_buf,len);
	
}


void uart_rx_idle_callback(UART_HandleTypeDef *huart)
{

	__HAL_UART_CLEAR_IDLEFLAG(huart);
	if (huart == &huart6)
	{	
		__HAL_DMA_DISABLE(huart->hdmarx);
		
		if(action_receive_buffer[0]==0x0D&&action_receive_buffer[27]==0x0D) 
		{
			if(action_receive_buffer[1]==0x0A&&action_receive_buffer[26]==0x0A)
			memcpy(&Action,action_receive_buffer,28);
			
			Action_now.x=(short)Action.y_buffer;
			Action_now.y=-(short)Action.x_buffer;
			Action_init_finish=1;
			
		}	
		HAL_UART_Receive_IT(&huart6, action_receive_buffer,28);	
	}

}

void uart_receive_handler(UART_HandleTypeDef *huart)
{
	if (__HAL_UART_GET_FLAG(huart, UART_FLAG_IDLE) &&
		__HAL_UART_GET_IT_SOURCE(huart, UART_IT_IDLE))
	{
		uart_rx_idle_callback(huart);
	}
}


void usart_init(void)
{  
  HAL_UART_Receive_IT(&huart6, action_receive_buffer, 28);
  __HAL_UART_ENABLE_IT(&huart6, UART_IT_IDLE);
  __HAL_UART_ENABLE(&huart6);

}



void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if (huart == &huart1)
	{
		if(Rx_buffer>1 && Rx_buffer<flag_data.special_point_num+2)
		{
		  expect_position=Rx_buffer-1;
			buzzer_flag=5;
		}
		
		
		HAL_UART_Receive_IT(&huart1,&Rx_buffer, 1);
	}
}





